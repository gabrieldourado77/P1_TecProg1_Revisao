public class Metodo {
    
    /*
    Método - É uma ação, que pertence á uma classe, semelhante a uma função.

    Ele pode retornar um valor para o algoritmo principal ou não.

    Se tiver retorno, o tipo de retorno deve ser específicado e é necessário usar o comando "return".
    Senão, é usado "void", na hora de criar ele.
    */

    // Exemplo 1

    public String mostrarMsg(){
        return "Mensagem";
    }

    // Exemplo 2

    public void mostrarOlaMundo(){
        System.out.println("Olá, mundo!");
    }

}
